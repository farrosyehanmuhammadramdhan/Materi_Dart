/// Struktur Dart
void main(){
  print('hello World');
  
  /// type data integer
  int number = 10;
  print(number);

  /// type data double
  double number2 = 10.0;
  print(number2);

  /// type data string
  String name = 'Yehan';
  print(name);

  /// type data boolean
  bool isValue = true;
  print(isValue);

  /// type data Map
  Map person = {'name': 'Yehan', 'age': '17'};
  print(person);
  print(person['age']);
  
  /// type data set
  Set<int> name3 = {1,2,3,4};
  print(name3);
  

  
}